    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        jLabel5.setText(nowDate.nowDate());
        ArrayList<String> gs = null;
        try {
            gs = serv.GetGoods();
        } catch (RemoteException ex) {
            ex.printStackTrace();
        }
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        int i = 0;
        int max1=3;int max2=12;int max3=15;
        int min1=1;int min2=6;int min3=10;
        Random random = new Random();
        int m = random.nextInt(max2)%(max2-min2+1) + min2;       
        for (String goods : gs) {
            int p = random.nextInt(max1)%(max1-min1+1) + min1; 
            int d = random.nextInt(max3)%(max3-min3+1) + min3;
            String[] arr = goods.split("\n");
            tableModel.setValueAt(String.valueOf(i+1),i,0);
            tableModel.setValueAt(arr[1],i,1);
            tableModel.setValueAt(arr[3],i,2);
            tableModel.setValueAt(arr[4],i,3);
            tableModel.setValueAt(arr[5],i,4);
            tableModel.setValueAt(String.valueOf(p),i,5);
            tableModel.setValueAt(arr[0]+"-"+String.valueOf(p),i,6);
            tableModel.setValueAt("2017"+"-"+String.valueOf(m)+"-"+String.valueOf(d),i,7);            
            i++;
        }
        jTable1.invalidate();
    }//GEN-LAST:event_formWindowOpened

