    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
         String[] Date=jFormattedTextField1.getText().split("\\.");
        int duration=Integer.parseInt(jFormattedTextField2.getText());
        List<client_p> cps=new ArrayList<client_p>();
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        int i=0;
        while ((String) tableModel.getValueAt(i, 0)!=null) {
            client_p cp = new client_p();
            cp.setUserLevel(Integer.parseInt((String) tableModel.getValueAt(i, 0)));// 取得第i行第一列的数据
            cp.setG_ID((String) tableModel.getValueAt(i, 1));// 取得第i行第二列的数据
            cp.setDiscount(Double.parseDouble((String) tableModel.getValueAt(i, 2)));// 取得第i行第三列的数据
            cp.setVoucher(Double.parseDouble((String) tableModel.getValueAt(i, 3)));// 取得第i行第四列的数据
            cps.add(cp);
            i++;
        }
        try {
            serv.CP_D(Date, duration, cps);
            JOptionPane.showMessageDialog(this,"Create SP Successfully!","Message",JOptionPane.INFORMATION_MESSAGE);
            } catch (RemoteException ex) {
            Logger.getLogger(SPP_Dev.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton8ActionPerformed

