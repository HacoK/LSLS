    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        String ID=jTextField1.getText();
        StringBuilder stb=new StringBuilder();
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        int selectRows=jTable1.getSelectedRows().length;
        if (selectRows == 1) {
            int selectedRowIndex = jTable1.getSelectedRow();
            stb.append((String) tableModel.getValueAt(selectedRowIndex, 0));
            stb.append(" ");
            stb.append((String) tableModel.getValueAt(selectedRowIndex, 1));
            stb.append(" ");
            stb.append((String) tableModel.getValueAt(selectedRowIndex, 2));
            stb.append(" ");
            stb.append((String) tableModel.getValueAt(selectedRowIndex, 3));
            stb.append(" ");
            stb.append((String) tableModel.getValueAt(selectedRowIndex, 4));
            stb.append(" ");
            stb.append((String) tableModel.getValueAt(selectedRowIndex, 5));
        } else {
            int[] selRowIndexs = jTable1.getSelectedRows();
            for (int i = 0; i < selRowIndexs.length; i++) {
                stb.append((String) tableModel.getValueAt(selRowIndexs[i], 0));
                stb.append(" ");
                stb.append((String) tableModel.getValueAt(selRowIndexs[i], 1));
                stb.append(" ");
                stb.append((String) tableModel.getValueAt(selRowIndexs[i], 2));
                stb.append(" ");
                stb.append((String) tableModel.getValueAt(selRowIndexs[i], 3));
                stb.append(" ");
                stb.append((String) tableModel.getValueAt(selRowIndexs[i], 4));
                stb.append(" ");
                stb.append((String) tableModel.getValueAt(selRowIndexs[i], 5));
                if (i != selRowIndexs.length - 1) {
                    stb.append("\n");
                }
            }
        }
        try {
            serv.WarningCreate(ID, stb.toString());
            JOptionPane.showMessageDialog(this,"Submit Successfully!","Message",JOptionPane.INFORMATION_MESSAGE);
            dispose();
        } catch (RemoteException ex) {
            Logger.getLogger(Donate.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1MouseClicked

    private void jLabel7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel7MouseClicked
        // TODO add your handling code here:
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        int i=0;
        while((String)tableModel.getValueAt(i, 0)!=null){
            int sub=Integer.parseInt((String) tableModel.getValueAt(i, 3))-Integer.parseInt((String) tableModel.getValueAt(i, 4));
            tableModel.setValueAt(String.valueOf(sub), i, 5);
            i++;
        }
    }//GEN-LAST:event_jLabel7MouseClicked

